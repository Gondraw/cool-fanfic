		<center><font face="Enchanted Land" color="#fff" size="6"><p id="parrafo">1-¿Qué es Cool Fanfic? 
Es un sitio web dedicado al alojamiento de historias creadas por las personas quienes tienen que registrarse para realizar esta función creándose una cuenta de usuario, pero si lo que deseas es leer las historias que publican los usuarios no es necesario que te registres en nuestro sitio.
<br><br>
2-¿Es necesario Registrarme?
Como explicamos anteriormente, no es necesario que te registres para poder acceder al contenido que publican los usuarios pero si es necesario para tener la opción de subscribirte a los perfiles de otros usuarios, guardar las historias que te gustan para cuando la quieras volver a leer y por ultimo dar tu opinión comentando el contenido publicado por el usuario.
<br><br>
3-¿Cuáles son las normas de la comunidad de Cool Fanfic?
Las normas de la comunidad por el momento aún se están elaborando, ya que esto es algo muy importante para mantener el equilibrio de la página y nuestros usuarios y algunas decisiones tienen que tomarse con delicadeza. Por el momento lo único recomendado es que los usuarios no empleen el uso de Bots ya que los usuarios serán vaneados al instante de nuestro sitio y también mantener un buen comportamiento en la comunidad.
<br><br>
4-¿Qué tipo de historias son aceptadas en Cool Fanfic?
Las historias más aceptadas son las que se pueden encontrar en la sección de categorías, las únicas historias que no serían aceptadas son aquellas que cuentan hechos de desnudo o las que son dedicados a estos totalmente, los lectores podrán reportar este tipo de acciones en nuestro comunidad. 
<br><br>
5-¿Cómo puedo obtener ingresos si publico historia?
Esta opción se hace activando la monetización de tu perfil o cuenta de Cool Fanfic, vas a poder obtener ingresos mediante el sistema de lo que viene siendo CPC.
<br><br>
6-¿Qué es el sistema  CPC? 
El CPC de página, viene significando (COSTE POR CLICK) esto quiere decir que cantidad de ingresos adquirirás según los Clicks que los usuarios hagan en la publicidad que colocamos en las publicaciones de tus historias. EL CPC  normalmente es de $0.02, por lo tanto mientras más clicks tengan los anuncios que colocamos, más ingresos obtendrás, solo acordamos que no se puede emplear el uso de Bots ya que esto es una forma ilegal de general tráfico en la publicidad.
<br><br>
7-¿Cuáles métodos de pagos están disponibles?
Por el momento los métodos de pagos aceptados son por PayPal, ya vamos a crear poner más opciones para trasladar los ingresos adquiridos.
<br><br>
Si no hemos resorbidos tus dudas puedes contactarnos a nuestro correo electrónico.
